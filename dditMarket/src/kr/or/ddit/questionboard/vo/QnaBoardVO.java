package kr.or.ddit.questionboard.vo;

public class QnaBoardVO {
	private int qnaNo; /* QnA 번호 */
	private String adminId; /* 관리자ID */
	private String qnaTitle; /* QnA 제목  */
	private String qnaContent; /* QnA 내용 */
	private String qnaDy; /* 작성일자 */
	private String qnaDelyn; /* 삭제여부*/
	public int getQnaNo() {
		return qnaNo;
	}
	public void setQnaNo(int qnaNo) {
		this.qnaNo = qnaNo;
	}
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	public String getQnaTitle() {
		return qnaTitle;
	}
	public void setQnaTitle(String qnaTitle) {
		this.qnaTitle = qnaTitle;
	}
	public String getQnaContent() {
		return qnaContent;
	}
	public void setQnaContent(String qnaContent) {
		this.qnaContent = qnaContent;
	}
	public String getQnaDy() {
		return qnaDy;
	}
	public void setQnaDy(String qnaDy) {
		this.qnaDy = qnaDy;
	}
	public String getQnaDelyn() {
		return qnaDelyn;
	}
	public void setQnaDelyn(String qnaDelyn) {
		this.qnaDelyn = qnaDelyn;
	}
	@Override
	public String toString() {
		return "QnaBoardVO [qnaNo=" + qnaNo + ", adminId=" + adminId + ", qnaTitle=" + qnaTitle + ", qnaContent="
				+ qnaContent + ", qnaDy=" + qnaDy + ", qnaDelyn=" + qnaDelyn + "]";
	}
	
	
}
